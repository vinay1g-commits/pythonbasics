# Python supports standard arithmetic and logical operations.

a=10
b=5

print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b) #exponentiaion a^b

# comparison operators:
print(a>b)
print(a<b)
print(a==b)
print(a!=b)

#Logical operators:
print(a>0 and b>0)
print(a<0 or a>2)
print(not (a>b))

"""
Arithmetic operators perform mathematical operations, 
while comparison operators return boolean values (True or False). 
Logical operators combine conditions.
"""